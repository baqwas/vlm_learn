"""
analyze_stock_chart.py

Multimodal reasoning script using the Qwen3-VL-2B-Thinking model for chart analysis.

This script demonstrates:
1. Loading an image from a local file path (stock_price_chart.png).
2. Constructing a multimodal prompt (Image + Text) for reasoning.
3. Explicitly configuring the smallest reasoning model for CPU-only inference.

DEVELOPER NOTES:
----------------
- MODEL_ID: 'Qwen/Qwen3-VL-2B-Thinking' is the smallest variant of the Qwen3-VL series with enhanced reasoning capabilities.
- DEVICE: 'cpu' forces the model to load onto system RAM (CPU memory), as no CUDA is available.
- DTYPE: torch.float32 is used for maximum CPU compatibility, as many CPUs do not support native bfloat16 or float16 operations.
- **CRITICAL WARNING**: The 2B parameter model, even with float32 (which is ~8GB of memory just for weights), is fundamentally incompatible with the 32 MB RAM constraint mentioned. This script will require several GBs of system RAM to run successfully. The setting is purely for demonstration of the required *code configuration* for a low-resource environment.
- INFERENCE SPEED: Expect inference to be extremely slow (seconds to minutes per token) due to the CPU-only execution of a large model.
"""
import torch
from PIL import Image
from transformers import Qwen3VLForConditionalGeneration, AutoProcessor
import os
from typing import Union

# --- Configuration for CPU-Only and Thinking Model ---
MODEL_ID: str = "Qwen/Qwen3-VL-2B-Thinking"
DEVICE: str = "cpu"
DTYPE = torch.float32

# The local image file generated by the previous script
LOCAL_IMAGE_PATH: str = "../images/stock_price_chart.png"

# The specific prompt for the multimodal reasoning task
CHART_PROMPT: str = (
    "Analyze the stock performance chart. Which company had the best 6-month return, and what can be inferred from the shape of the lines regarding investor sentiment?"
)


def load_local_image(file_path: str) -> Union[Image.Image, None]:
    """Reads an image from a local file path and returns a PIL Image object."""
    print(f"Attempting to load image from: {file_path}")
    if not os.path.exists(file_path):
        print(f"Error: Local image file not found at path: {file_path}")
        return None

    try:
        # Use PIL's Image.open() for local file reading
        image = Image.open(file_path).convert("RGB")
        print("✅ Image loaded successfully.")
        return image
    except Exception as e:
        print(f"Error loading image file: {e}")
        return None


def run_chart_reasoning(model_id: str, device: str, image_path: str, chart_prompt: str):
    """Loads the model, prepares the multimodal input, and performs inference."""

    # --- 1. Load Model and Processor ---
    print(
        f"\n🧠 Loading model {model_id} to {device} (WARNING: Requires several GBs of RAM)..."
    )
    try:
        model = Qwen3VLForConditionalGeneration.from_pretrained(
            model_id,
            device_map=device,
            # Note: dtype is set to float32 to enforce CPU compatibility
            dtype=DTYPE,
            trust_remote_code=True,
        )
        processor = AutoProcessor.from_pretrained(model_id, trust_remote_code=True)
        print("✅ Model and Processor loaded successfully.")
    except Exception as e:
        print(f"\nFATAL MODEL LOADING ERROR: {e}")
        print(
            "This often happens when memory limits are exceeded or dependencies are missing."
        )
        return

    # --- 2. Prepare Multimodal Input (Image + Text) ---
    image = load_local_image(image_path)
    if image is None:
        return

    # Define the conversation structure for multimodal input
    messages = [
        {
            "role": "user",
            "content": [
                {"type": "image", "image": image},  # The image object
                {"type": "text", "text": chart_prompt},  # The prompt
            ],
        }
    ]

    # Apply the chat template and tokenize the input
    inputs = processor.apply_chat_template(
        messages,
        tokenize=True,
        add_generation_prompt=True,
        return_dict=True,
        return_tensors="pt",
    )

    # Move inputs to the designated device (CPU)
    inputs = inputs.to(model.device)

    print("\n⏳ Starting CPU inference. Expect very long latency...")

    # --- 3. Inference: Generation of the Output ---
    try:
        generated_ids = model.generate(
            **inputs,
            max_new_tokens=512,
            do_sample=False,  # Prefer deterministic, reasoned output
            temperature=0.01,  # Low temperature for logical precision
        )
    except Exception as e:
        print(f"\nFATAL INFERENCE ERROR: {e}")
        print("Inference failed, likely due to out-of-memory or timeout on CPU.")
        return

    # Decode the generated IDs, skipping the input prompt tokens
    generated_ids_trimmed = generated_ids[0][len(inputs.input_ids[0]) :].tolist()
    response = processor.decode(generated_ids_trimmed, skip_special_tokens=True)

    # --- 4. Print Output ---
    print("\n" + "=" * 50)
    print(f"REQUESTED PROBLEM: {chart_prompt}")
    print("\n🤖 Qwen3-VL Reasoning Response:")
    print(response.strip())
    print("=" * 50)


if __name__ == "__main__":
    try:
        run_chart_reasoning(MODEL_ID, DEVICE, LOCAL_IMAGE_PATH, CHART_PROMPT)
    except Exception as e:
        print(f"An unexpected error occurred: {e}")
