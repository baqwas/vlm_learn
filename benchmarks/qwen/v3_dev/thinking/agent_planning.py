import time  # Used to simulate the model's "thinking" time


def generate_agent_plan(visual_input_type: str, user_goal: str) -> str:
    """
    Simulates the Qwen3-VL-Thinking model's generation of a multi-step plan.

    Args:
        visual_input_type (str): The type of visual input (e.g., 'Chart', 'GUI Screenshot').
        user_goal (str): The complex task the agent needs to achieve.

    Returns:
        str: A structured, executable action plan.
    """
    print(f"--- Agent Planning Started ---")
    print(f"Goal received: {user_goal}")
    print(f"Input type: {visual_input_type}")
    print("\nSimulating Qwen3-VL-Thinking's deep analysis...")
    time.sleep(1.5)  # A brief pause to simulate the computational cost of "Thinking"

    # The key to the Thinking model: Structured, multi-step output
    plan = f"""
[AGENT PLAN GENERATED BY QWEN3-VL-THINKING]

GOAL: {user_goal}
INPUT: {visual_input_type}

STEP 1: VISUAL PERCEPTION (Observation)
   - Action: Invoke Multimodal OCR/Grounding tool.
   - Target: Scan the {visual_input_type} to identify key elements related to the goal.
   - Output: Extract text labels, chart values, and coordinate bounding boxes for key entities.

STEP 2: LOGICAL REASONING (Analysis)
   - Action: Process extracted data using internal reasoning trace.
   - Sub-Steps:
     a) Cross-reference the "product names" (from labels) with "sales figures" (from values).
     b) Identify the maximum sales value from the Q4 data set.
     c) Determine the corresponding product name for that maximum value.
   - Output: The name of the top-selling product in Q4.

STEP 3: ACTION & FORMATTING (Execution)
   - Action: Format the final, verified result for the user.
   - Format: Return the product name and its sales figure in a clean, human-readable sentence.

[PLANNING COMPLETE: Ready for Tool Execution]
"""
    return plan.strip()


# --- DEMONSTRATION EXAMPLES ---

# Scenario 1: Complex Multimodal Data Extraction
goal_1 = "Find the top-selling product name and its total sales figure from the Q4 data in the uploaded bar chart."
plan_1 = generate_agent_plan("Complex Bar Chart Image", goal_1)

print("\n" + "=" * 80)
print(plan_1)
print("=" * 80)

# Scenario 2: Agent Planning for a UI Task
print("\n" + "=" * 80)
goal_2 = "Open the settings menu and change the user avatar using the icons visible on the mobile screen screenshot."
plan_2 = generate_agent_plan("Mobile App Screenshot (GUI)", goal_2)

print("\n" + "=" * 80)
print(plan_2)
print("=" * 80)
